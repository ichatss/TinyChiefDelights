package com.tinychiefdelights.model;

public class Review {

    // Поля
    private String review;

    private float rate;

    // Методы
    public String getReview() {
        return review;
    }

    public void setReview(String review) {
        this.review = review;
    }

    public float getRate() {
        return rate;
    }

    public void setRate(float rate) {
        this.rate = rate;
    }
}
